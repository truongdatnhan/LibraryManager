create database Thuvien;
use Thuvien;
-- table nhân viên
create table NHANVIEN(
	MANV CHAR(10) NOT NULL,
    HONV NVARCHAR(50) NOT NULL,
    TENNV NVARCHAR(50) NOT NULL,
    NGAYSINH date,
    GIOITINH CHAR(5) CHECK ( GIOITINH IN("Nam","Nữ")),
    DIACHINV NVARCHAR(50),
    EMAILNV NVARCHAR(50),
    SDTNV CHAR(10) ,
    LUONG INT,
    TRANGTHAI INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table sách 
CREATE TABLE SACH(
	MASACH CHAR(10) NOT NULL,
    TENSACH NVARCHAR(50) NOT NULL,
    GIASACH INT,
    MATHELOAI CHAR(10),
    MATG CHAR(10),
    MANXB CHAR(10),
    MALINHVUC CHAR(10),
    HINHANH CHAR(50),
    SOLUONG INT,
    TRANGTHAI INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table đọc giả
CREATE TABLE DOCGIA (
	MADG CHAR(10) NOT NULL,
    HODG NVARCHAR(50) NOT NULL,
    TENDG NVARCHAR(50) NOT NULL,
    NGAYSINHDG date,
    DIACHIDG NVARCHAR(150),
    NGHENGHIEP NVARCHAR(30),
    TRINHDO NVARCHAR(50),
    TRANGTHAI INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table thẻ thư viện
CREATE TABLE THETHUVIEN (
	MATHE CHAR(10) NOT NULL,
    MADG CHAR(10) NOT NULL,
    NGAYCAP date,
    NGAYHETHAN date,
    TRANGTHAI INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table người dùng
CREATE TABLE NGUOIDUNG(
	MANV CHAR(10) NOT NULL,
    MATKHAU NVARCHAR(50) NOT NULL,
    MAQUYEN CHAR(10) NOT NULL,
    TRANGTHAI INT 
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table phân quyền
CREATE TABLE PHANQUYEN(
	MAQUYEN CHAR(10) NOT NULL,
    TENQUYEN NVARCHAR(30) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table nhà cung cấp
CREATE TABLE NCC(
	MANCC CHAR(10) NOT NULL,
    TENNCC NVARCHAR(50),
    DIACHINCC NVARCHAR(50),
    SDTNCC CHAR(10),
    TRANGTHAI INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table lĩnh vực
CREATE TABLE LINHVUC(
	MALINHVUC CHAR(10) NOT NULL,
    TENLINHVUC NVARCHAR(50) NOT NULL,
    TRANGTHAI INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table thể loại
CREATE TABLE THELOAI (
	MATHELOAI CHAR(10) NOT NULL,
    TENTHELOAI NVARCHAR(50) NOT NULL,
    TRANGTHAI INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table nhà xuất bản
CREATE TABLE NXB(
	MANXB CHAR(10) NOT NULL,
    TENNXB NVARCHAR(50) NOT NULL,
    SDTNXB CHAR(10),
    EMAILNXB NVARCHAR(50),
    TRANGTHAI INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table tác giả 
CREATE TABLE TACGIA(
	MATG CHAR(10) NOT NULL,
    HOTG NVARCHAR(50) NOT NULL,
    TENTG NVARCHAR(20) NOT NULL,
    EMAILTG NVARCHAR(50),
    TRANGTHAI INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table phiếu nhập
CREATE TABLE PHIEUNHAP(
	MAPN CHAR(10) NOT NULL,
    MANV CHAR(10) NOT NULL,
	MANCC CHAR(10) NOT NULL,
	NGAYNHAP date NOT NULL,
    TONGTIEN INT NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table chi tiết phiếu nhập
CREATE TABLE CTPHIEUNHAP(
	MAPN CHAR(10) NOT NULL,
    MASACH CHAR(10) NOT NULL,
    SOLUONG INT ,
    DONGIA INT,
    THANHTIEN INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table phiếu mượn
CREATE TABLE  PHIEUMUON(
	MAPM CHAR(10) NOT NULL,
    MANV CHAR (10) NOT NULL,
    MATHE CHAR(10) NOT NULL,
    NGAYMUON date,
    NGAYQUIDINHTRA date
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table chi tiết phiếu mượn 
CREATE TABLE CTPHIEUMUON(
	MAPM CHAR(10) NOT NULL,
    MASACH CHAR(10) NOT NULL,
    SOLUONG INT ,
    TINHTRANG NVARCHAR(50),
    NGAYTHUCTRA date
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- table phiếu phạt
CREATE TABLE PHIEUPHAT(
	MAPP CHAR(10) NOT NULL,
    MAPM CHAR(10) NOT NULL,
	MAQD CHAR(10),
    TIENPHAT INT,
